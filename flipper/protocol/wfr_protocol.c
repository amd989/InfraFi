uint32_t wfr_crc32(const uint8_t* data, size_t len) {
    uint32_t crc = 0xFFFFFFFF;
    for(size_t i = 0; i < len; i++) {
        crc = crc32_table[(crc ^ data[i]) & 0xFF] ^ (crc >> 8);
    }
    return crc ^ 0xFFFFFFFF;
}

/* Characters that must be backslash-escaped in WiFi QR strings */
static bool wfr_needs_escape(char c) {
    return c == '\\' || c == ';' || c == ':' || c == '"' || c == ',';
}

static size_t wfr_escape_append(char* out, size_t pos, size_t max, const char* str) {
    for(size_t i = 0; str[i] && pos < max - 1; i++) {
        if(wfr_needs_escape(str[i])) {
            if(pos + 2 > max - 1) break;
            out[pos++] = '\\';
        }
        out[pos++] = str[i];
    }
    return pos;
}

size_t wfr_build_wifi_string(const WfrWifiCreds* creds, char* out, size_t out_size) {
    if(!creds || !out || out_size < 16) return 0;

    const char* sec_str;
    switch(creds->security) {
    case WFR_SEC_WPA: sec_str = "WPA"; break;
    case WFR_SEC_WEP: sec_str = "WEP"; break;
    case WFR_SEC_SAE: sec_str = "SAE"; break;
    default: sec_str = "nopass"; break;
    }

    size_t pos = 0;

    /* WIFI:T:<type>;S: */
    const char* prefix = "WIFI:T:";
    size_t plen = strlen(prefix);
    if(pos + plen >= out_size) return 0;
    memcpy(out + pos, prefix, plen);
    pos += plen;

    size_t slen = strlen(sec_str);
    if(pos + slen + 3 >= out_size) return 0;
    memcpy(out + pos, sec_str, slen);
    pos += slen;

    out[pos++] = ';';
    out[pos++] = 'S';
    out[pos++] = ':';

    /* Escaped SSID */
    pos = wfr_escape_append(out, pos, out_size, creds->ssid);

    /* ;P:<password> */
    if(pos + 3 >= out_size) return 0;
    out[pos++] = ';';
    out[pos++] = 'P';
    out[pos++] = ':';

    pos = wfr_escape_append(out, pos, out_size, creds->password);

    /* ;H:<hidden>;; */
    const char* hidden_str = creds->hidden ? ";H:true;;" : ";H:false;;";
    size_t hlen = strlen(hidden_str);
    if(pos + hlen >= out_size) return 0;
    memcpy(out + pos, hidden_str, hlen);
    pos += hlen;

    out[pos] = '\0';
    return pos;
}

/*
 * Extract a field value from a WiFi QR string.
 * Looks for "key:" and reads until unescaped ';'.
 * Handles backslash escaping. Returns bytes written to out, or -1 if not found.
 */
static int wfr_extract_field(const char* str, const char* key, char* out, size_t out_size) {
    const char* p = str;
    size_t klen = strlen(key);

    while(*p) {
        if(strncmp(p, key, klen) == 0) {
            p += klen;
            size_t pos = 0;
            while(*p && pos < out_size - 1) {
                if(*p == '\\' && *(p + 1)) {
                    p++;
                    out[pos++] = *p++;
                } else if(*p == ';') {
                    break;
                } else {
                    out[pos++] = *p++;
                }
            }
            out[pos] = '\0';
            return (int)pos;
        }
        p++;
    }
    return -1;
}

bool wfr_parse_wifi_string(const char* str, WfrWifiCreds* creds) {
    if(!str || !creds) return false;

    /* Must start with WIFI: */
    if(strncmp(str, "WIFI:", 5) != 0) return false;

    memset(creds, 0, sizeof(WfrWifiCreds));

    /* Extract security type */
    char type_buf[16] = {0};
    if(wfr_extract_field(str, "T:", type_buf, sizeof(type_buf)) < 0) {
        return false;
    }

    if(strcmp(type_buf, "WPA") == 0) {
        creds->security = WFR_SEC_WPA;
    } else if(strcmp(type_buf, "WEP") == 0) {
        creds->security = WFR_SEC_WEP;
    } else if(strcmp(type_buf, "SAE") == 0) {
        creds->security = WFR_SEC_SAE;
    } else {
        creds->security = WFR_SEC_OPEN;
    }

    /* Extract SSID (required) */
    if(wfr_extract_field(str, "S:", creds->ssid, sizeof(creds->ssid)) < 0) {
        return false;
    }
    if(creds->ssid[0] == '\0') return false;

    /* Extract password (optional for open networks) */
    wfr_extract_field(str, "P:", creds->password, sizeof(creds->password));

    /* Extract hidden flag (optional, defaults to false) */
    char hidden_buf[8] = {0};
    if(wfr_extract_field(str, "H:", hidden_buf, sizeof(hidden_buf)) >= 0) {
        creds->hidden = (strcmp(hidden_buf, "true") == 0);
    }

    return true;
}
